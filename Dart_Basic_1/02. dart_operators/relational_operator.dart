void main(List<String> args) {
  int a = 5;
  int b = 10;

  var res1 = (a > b);
  print('a is greater than b :$res1');
  var res2 = (a < b);
  print('a less than b: $res2');
  var res3 = (a >= b); //greater than or equal to
  print('a less than b: $res3');
  var res4 = (a <= b); //less than or equal to
  print('a less than b: $res4');

  var res5 = (a == b); //equal to equal
  print('a is equal to equal:$res5 ');

  var res6 = (a != b);
  print('a is not equal: $res6');
  
}
