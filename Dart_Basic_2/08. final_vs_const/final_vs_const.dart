void main() {
//Normal
  int a = 10;
  a = 15;
  print('Normal Variable: $a');

//final kayword
  int b = 5;
  final c = 10; //Exmple: c = 20; Can't Change final keyword value.
  print('Final kayword : $c');

//const kayword
  int d = 20;
  const e = 50; //(v = e;) (f = 50;) can't change the value & can't assigned.
  print('const keyword : $e');
}
